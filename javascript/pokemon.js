class Pokemon {
    constructor(name, type, moves, image, hp, attack, defense, sp_attack, sp_defemse, speed) {
        this.name = name
        this.type = type;
        this.moves = moves;
        this.image = image;
        this.level = 100;
        this.condition = null;
        this.hp = hp;
        this.attack = attack;
        this.defense = defense;
        this.sp_attack = sp_attack;
        this.sp_defemse = sp_defemse;
        this.speed = speed;
    }
}